namespace Spice86.Shared.Emulator.Mouse;

/// <summary>
/// Identify each mouse button
/// </summary>
public enum MouseButton {
    /// <summary>
    /// No mouse button
    /// </summary>
    None,

    /// <summary>
    /// The left mouse button
    /// </summary>
    Left,

    /// <summary>
    /// The right mouse button
    /// </summary>
    Right,

    /// <summary>
    /// The middle mouse button
    /// </summary>
    Middle,

    /// <summary>
    /// Extra button 1
    /// </summary>
    XButton1,

    /// <summary>
    /// Extra button 2
    /// </summary>
    XButton2
}